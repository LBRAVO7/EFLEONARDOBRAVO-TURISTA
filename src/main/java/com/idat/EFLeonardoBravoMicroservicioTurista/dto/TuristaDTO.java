package com.idat.EFLeonardoBravoMicroservicioTurista.dto;



public class TuristaDTO {
	
	private Integer cod;
	private String nomturista;
	private String numcel;
	private String direc;
	
	
	public Integer getCod() {
		return cod;
	}
	public void setCod(Integer cod) {
		this.cod = cod;
	}
	public String getNomturista() {
		return nomturista;
	}
	public void setNomturista(String nomturista) {
		this.nomturista = nomturista;
	}
	public String getNumcel() {
		return numcel;
	}
	public void setNumcel(String numcel) {
		this.numcel = numcel;
	}
	public String getDirec() {
		return direc;
	}
	public void setDirec(String direc) {
		this.direc = direc;
	}
	
	
	

}
